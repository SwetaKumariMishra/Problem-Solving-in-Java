public class Right_triangle_Star {
    public static void main(String[] args) {
        {
            int i,j,k=5;
            for(i=0; i<k; i++)
            {
                for(j=0; j<=i; j++)
                {
                    System.out.print("*");
                }
                System.out.println();
            }
        }
    }
}
